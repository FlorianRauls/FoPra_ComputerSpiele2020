using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Profile
{
    private int id;
    private string name;
    private int volume;
    private int currentLevel;
    private int maxLevel;
    private bool autoRespawn;

    public GetID()
    {
        return id;
    }

    public GetName()
    {
        return name;
    }

    public GetVolume()
    {
        return volume;
    }

    public GetCurrentLevel()
    {
        return currentLevel;
    }

    public GetMaxLevel()
    {
        return maxLevel;
    }

    public GetAutoRespawn()
    {
        return autpRespawn;
    }
}
